    ID      BIGINT       NOT NULL AUTO_INCREMENT,
    CID      BIGINT      NULL,
    NOME    VARCHAR(100) NOT NULL,
    CPF     VARCHAR(14)  NULL,
    SENHA   CHAR(4)      NOT NULL,
    CONSTRAINT PK_CLIENTE PRIMARY KEY (ID)
